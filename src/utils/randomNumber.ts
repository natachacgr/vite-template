function sfc32(a: number, b: number, c: number, d: number) {
  return function () {
    a |= 0;
    b |= 0;
    c |= 0;
    d |= 0;
    const t = (((a + b) | 0) + d) | 0;
    d = (d + 1) | 0;
    a = b ^ (b >>> 9);
    b = (c + (c << 3)) | 0;
    c = (c << 21) | (c >>> 11);
    c = (c + t) | 0;
    return (t >>> 0) / 4294967296;
  };
}

const r = () => Math.random();
const now = Date.now();
const prng = sfc32(now * r(), now * r(), now * r(), now * r());

export function randomInt(max = 1000) {
  return Math.floor(prng() * max);
}
